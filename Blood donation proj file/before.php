<h1 class="after"> AFTER<span style="color:Red ;font-size:xx-large; font-weight:bold;"> BLOOD DONATION </span></h1> 
                    <p class="om">


                    1) Expect to be asked to take some rest by sitting down for 10 to 20 minutes after giving blood. 
                    This lets the staff keep an eye on you to make sure that you are in good shape before you leave.<br>
                     2) Drink some water or juice and eat a light snack with a high sugar content to ensure that blood sugar levels increases. <br>
                     3) Avoid driving if you have the sensation of fainting. Donating blood is not dangerous, but driving is, and you could be seriously injured if you faint while driving. 
                     If you are not well after your donation, call someone to come pick you up. <br>
                     4) Eat food that is high in protein content after donation; legumes are excellent choices. You should also eat vegetables and be sure to drink plenty of water. All these things will help your body make more blood for you soon.<br>
                     5) Refrain from drinking alcohol for at least 12 hours after giving blood. <br>
                     6) Excuse yourself from heavy lifting for the rest of the day.<br>



</p>

