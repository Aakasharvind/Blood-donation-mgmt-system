                    <h1 class="before"> BEFORE <span style="color:Red ;font-size:xx-large; font-weight:bold;">BLOOD DONATION  </span></h1> 
                    <p class="om">
                    Prior to donating blood 1) Drink plenty of water or fruit juice the night and morning before you donate.
                     The leading cause of faintness and dizziness is a drop in blood pressure.
                      Getting lot of water or fruit juice into your body helps keep your blood pressure up.
                       If you are donating plasma or platelets, drink at least six to eight glasses.
                        Avoid drinking caffeinated drinks, as these will cause your body to expel water.<br>
                    2) Eat three hours prior to the donation. Its not a good idea to come with an empty stomach.
                 Eating will keep your blood sugar levels stable, helping you to feel better after you have donated blood.
          It also helps to ward off light headedness.
                        If you feel like having breakfast,eat something light such as (cereal, toast, etc.) or a light lunch, such as a sandwich and a fruit.
               If you think you might encounter nausea during donation dont eat immediately before donation.
                      Avoid fatty foods for 24 hours before you donate. 
                    Increased fat in your blood stream might make it impossible to get accurate readings on some of the mandatory screening tests conducted on samples of your blood after you donate.<br>
                   <br>  3) Wear loose and comfortable clothing and avoid tight sleeves.<br>
                      4) Get plenty of sleep on the night before you plan to donate.<br>
                       5) If you are a platelet donor, remember that you must not take aspirin for two days prior to donating.<br>
                        6) Answer questions from the staff members or on forms honestly.<br>
                     Certain activities increase the chances of blood borne illnesses (i.e. those that can be transmitted through blood) and those who have recently engaged in such activities may be prevented from donating blood for a period of time.<br>


</p>

