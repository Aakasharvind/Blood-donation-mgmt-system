                  <h1> Process of Blood Donation </h1>
               <h2>   Registration (10 Mins)</h2>
               <p class="info">
Donor fills up the registration form and gives his consent for donation.</p> 
<img src="1.png"><br> <br>

<h2> Medical Check up (5 Mins)</h2><p class="info">
Donors Medical History & life style is asked, check up of temperature, blood pressure, pulse and haemoglobin.</p>
<img src="2.png"><br> <br>



<h2> Donation (8 Mins)</h2><p class="info">
Phlebotomist draws unit (350ml/450ml) of blood. A new sterile meedle is used to draw each unit and the needle is destroyed after it has been used. It taked less than 10 min.
</p><img src="3.png"><br> <br>
