                <h1> Types of Blood Donation </h1>
                <h2> Whole Blood </h2>
                <p class="type"> 
                  
Whole blood donation is the most common and traditional method. <br>
 A pint of blood is drawn that contains red and white cells, platelets, and plasma.<br>
 It’s mainly used for trauma and surgery. Shelf life is 21-35 days, and you can donate every 56 days.<br>
  This is the quickest and easiest donation.<br><br>


</p>
<h2> Platelets </h2>
<p class="type">
Platelets are small cell fragments in the blood that helps stop or prevent bleeding.<br>
 It’s made in our bone marrow and its shelf life is only 5 days!<br>
  They are used for surgeries, transplants, and to help cancer patients get through chemo.<br>
 You can donate every seven days, only at our centers.<br><br>
</p>


<h2> Plasma </h2>
<p class="type">
Plasma is the liquid portion of your blood that transports water and nutrients to your body’s tissues. <br>
Donations can last up to 1 year, and are used to help burn victims and those with bleeding disorders.<br>
 You can donate every 28 days, only at our centers. <br><br>
</p>

<h2> Automated Red Cells </h2>
<p class="type">
Automated red cells donation is when only red cells are collected, not the other blood components. The donation lasts up to 42 days.
 Red cells are used for trauma, surgery, anemia, blood loss and disorders.
  You can donate every 112 days, in centers and select mobiles.

